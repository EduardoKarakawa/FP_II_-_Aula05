
//Imprimindo o tamanho em bytes dos tipos de dados

#include <iostream>

int main() {
	std::cout << "Char = " << sizeof(char) << std::endl;
	std::cout << "Int = " << sizeof(int) << std::endl;
	std::cout << "Float = " << sizeof(float) << std::endl;
	std::cout << "Double = " << sizeof(double) << std::endl;
	system("pause");
	return 0;
}